package timesTable.ver02Better;

import java.util.Scanner;

/**
 * Created by taemi on 2017-06-28.
 */
public class ScannerTimesTable {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        System.out.println("출력할 단을 입력하시오 : ");
        int input = scan.nextInt();
        System.out.println(input * 1);
        System.out.println(input * 2);
        System.out.println(input * 3);
        System.out.println(input * 4);
        System.out.println(input * 5);
        System.out.println(input * 6);
        System.out.println(input * 7);
        System.out.println(input * 8);
        System.out.println(input * 9);

    }
}
